package org.esy.base.common;

/**
 * 
 * 规则类型
 *
 * @author cx
 * @date 2016年9月13日 下午4:54:07 
 * @version v1.0
 */
public enum RulesType {

	/**
	 * 事件类型
	 */
	EVENT("E"),

	/**
	 * 手动类型
	 */
	HANDLE("H"),

	/**
	 * 定时器类型
	 */
	TIMES("T");

	private final String rtype;

	RulesType(String rtype) {
		this.rtype = rtype;
	}

	@Override
	public String toString() {
		return this.rtype;
	}
}
