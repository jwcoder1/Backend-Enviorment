package org.esy.base.core;

/**
 * @author qiongwei.cai
 * @version 创建时间：2016年3月17日 下午5:40:37 在此类设置格式化的规则
 */
public class FormatSettings {

	private String dateFormat;

	private String booleanflag;
	
	private Integer decimalCount;

	/**
	 * @return the decimalCount
	 */
	public Integer getDecimalCount() {
		return decimalCount;
	}

	/**
	 * @param decimalCount
	 *            设置double/float类型保留的小数位
	 */
	public void setDecimalCount(Integer decimalCount) {
		this.decimalCount = decimalCount;
	}

	/**
	 * @return the dateFormat
	 */
	public String getDateFormat() {
		return dateFormat;
	}

	/**
	 * @param dateFormat
	 *            日期格式化字符串,如:yyyy-MM-dd
	 */
	public void setDateFormat(String dateFormat) {
		this.dateFormat = dateFormat;
	}

	public String getBooleanflag() {
		return booleanflag;
	}
	
	/**
	 * 
	 * @param booleanflag
	 * 			设置布尔型的格式化,如:√,×
	 */
	public void setBooleanflag(String booleanflag) {
		this.booleanflag = booleanflag;
	}

}
