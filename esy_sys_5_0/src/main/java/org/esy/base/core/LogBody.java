package org.esy.base.core;

public class LogBody {

	private String moduleId;

	private String eventId;

	private String message;

	private String user;

	private String date;

	private String localIp;

	public LogBody() {
		super();
	}

	/**
	 * @param moduleId
	 * @param eventId
	 * @param message
	 * @param user
	 * @param date
	 * @param localIp
	 */
	public LogBody(String moduleId, String eventId, String message, String user, String date, String localIp) {
		super();
		this.moduleId = moduleId;
		this.eventId = eventId;
		this.message = message;
		this.user = user;
		this.date = date;
		this.localIp = localIp;
	}

	/**
	 * @return the moduleId
	 */
	public final String getModuleId() {
		return moduleId;
	}

	/**
	 * @param moduleId the moduleId to set
	 */
	public final void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}

	/**
	 * @return the eventId
	 */
	public final String getEventId() {
		return eventId;
	}

	/**
	 * @param eventId the eventId to set
	 */
	public final void setEventId(String eventId) {
		this.eventId = eventId;
	}

	/**
	 * @return the message
	 */
	public final String getMessage() {
		return message;
	}

	/**
	 * @param message the message to set
	 */
	public final void setMessage(String message) {
		this.message = message;
	}

	/**
	 * @return the user
	 */
	public final String getUser() {
		return user;
	}

	/**
	 * @param user the user to set
	 */
	public final void setUser(String user) {
		this.user = user;
	}

	/**
	 * @return the date
	 */
	public final String getDate() {
		return date;
	}

	/**
	 * @param date the date to set
	 */
	public final void setDate(String date) {
		this.date = date;
	}

	/**
	 * @return the localIp
	 */
	public final String getLocalIp() {
		return localIp;
	}

	/**
	 * @param localIp the localIp to set
	 */
	public final void setLocalIp(String localIp) {
		this.localIp = localIp;
	}

}
