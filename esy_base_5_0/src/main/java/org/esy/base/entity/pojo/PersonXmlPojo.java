package org.esy.base.entity.pojo;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * 转xml格式web service专用pojo
 * 
 * @author WYF 2017-01-04
 *
 */
public class PersonXmlPojo implements Serializable {

	private static final long serialVersionUID = 1L;

	private String eid;

	private String uid;

	private String employeeNo;

	private String pid;

	private String cname;

	private String py;

	private String pinyin;

	private String birthday;

	private String officePhone;

	private String mobilePhone;

	private String sex;

	private String mail;

	private String enable;

	private String seq;

	private String type;

	private String memo;

	private List<IdentityXmlPojo> identity;

	public String getEid() {
		return eid;
	}

	public void setEid(String eid) {
		this.eid = eid;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getEmployeeNo() {
		return employeeNo;
	}

	public void setEmployeeNo(String employeeNo) {
		this.employeeNo = employeeNo;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public String getPy() {
		return py;
	}

	public void setPy(String py) {
		this.py = py;
	}

	public String getPinyin() {
		return pinyin;
	}

	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}

	public String getBirthday() {
		return birthday;
	}

	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}

	public String getOfficePhone() {
		return officePhone;
	}

	public void setOfficePhone(String officePhone) {
		this.officePhone = officePhone;
	}

	public String getMobilePhone() {
		return mobilePhone;
	}

	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getEnable() {
		return enable;
	}

	public void setEnable(String enable) {
		this.enable = enable;
	}

	public String getSeq() {
		return seq;
	}

	public void setSeq(String seq) {
		this.seq = seq;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public List<IdentityXmlPojo> getIdentity() {
		if (identity == null) {
			identity = new ArrayList<IdentityXmlPojo>();
		}
		return identity;
	}

	public void setIdentity(List<IdentityXmlPojo> identity) {
		this.identity = identity;
	}

}
