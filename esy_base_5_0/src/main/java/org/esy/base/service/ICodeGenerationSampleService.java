package org.esy.base.service;

import org.esy.base.core.IBaseService;
import org.esy.base.entity.CodeGenerationSample;

/**
 * 
 * Dao for 自动代码生成范例
 *
 */
public interface ICodeGenerationSampleService extends IBaseService<CodeGenerationSample> {

	/**
	 *
	 * 根据编号取得实例
	 *
	 * @param id
	 *			编号
	 * 
	 * @return 得到的实例
	 */
	public CodeGenerationSample getEntityById(String id);
	
	/**
	 *
	 * 根据两个参数返回值
	 *
	 * @param parm1
	 *			参数1
	 * 
	 * @param parm2
	 *			参数2
	 * 
	 * @return 计算后的返回值
	 */
	public int getEntityByNumber(int parm1, int parm2);
	
}
